/* In Dart, simple integer operators are fundamental arithmetic operations 
used to perform calculations on integer values. Dart provides several basic 
arithmetic operators for integers, including addition, subtraction, 
multiplication, division, modulo, increment, and decrement. */

// Here's a comprehensive explanation of each simple integer operator in Dart:

// 1. Addition (+):
// The addition operator ('+') is used to add two integers together.
int result1 = 10 + 5; // result will be 15

// 2. Subtraction (-):
// The subtraction operator ('-') is used to subtract one integer from another.
int result2 = 10 - 5; // result will be 5

// 3. Multiplication (*):
// The multiplication operator ('*') is used to multiply two integers.
int result3 = 10 * 5; // result will be 50

// 4. Division (/):
// The division operator ('/') is used to divide one integer by another. It performs floating-point division.
double result4 = 10 / 5; // result will be 2.0

// 5. Modulo (%):
// The modulo operator ('%') returns the remainder of the division of one integer by another.
int result5 =
    10 % 3; // result will be 1 (10 divided by 3 equals 3 with a remainder of 1)

// 6. Increment (++):
// The increment operator ('++') is used to increase the value of an integer by 1.
int number6 = 5; // number6++; // number will be 6

// 7. Decrement (--):
// The decrement operator ('--') is used to decrease the value of an integer by 1.
int number7 = 5; // number7--; // number will be 4

// 8. Ttruncating division (~/):
// The '~/' operator divides two integers and returns an integer result, truncating any fractional part.
int result8 = 10 ~/ 3; // result will be 3
